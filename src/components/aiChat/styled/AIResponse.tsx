import AnimatedText from "../../common/text/AnimatedText";
import { GLOBAL_COLORS, GLOBAL_FONTS } from "src/theme";

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const MainContainer = styled.div`
  position: relative;
  width: 100%;
`;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////    MAIN COMPONENT   ////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

interface AIResponseProps {
  responseText: string;
  textSize: string;
  handleAIResponseEnd: () => void;
}

function AIResponse({
  responseText,
  textSize,
  handleAIResponseEnd,
}: AIResponseProps) {
  return (
    <MainContainer>
      <AnimatedText
        text={responseText}
        fontSize={textSize}
        animationTime={10}
        fontFamily={GLOBAL_FONTS.aiChat.aiText}
        color={GLOBAL_COLORS.text.aiChat.aiText}
        onEnd={handleAIResponseEnd}
      ></AnimatedText>
    </MainContainer>
  );
}

export default AIResponse;
